    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if(cbSearch.getSelectedItem() == "MaXL"){
            model.setRowCount(0); 
            try {
                List<xuly> xulyList = xlBLL.getXulyBySimilarMaxl(txtSearch.getText());
                for(xuly xuly : xulyList) {
                    Object[] row = {xuly.getMaXL(), xuly.getMaTV(), xuly.getHinhThucXL(), xuly.getSoTien() , xuly.getNgayXL(), xuly.getTrangThaiXL()};
                    model.addRow(row);
                }
                } catch (NumberFormatException e) {
                        JOptionPane.showMessageDialog(null, "Vui lòng nhập số nguyên.", "Lỗi", JOptionPane.ERROR_MESSAGE);
                  }
        }else{
            model.setRowCount(0); 
            try {
                List<xuly> xulyList = xlBLL.getXulyBySimilarMatv(txtSearch.getText());
                for(xuly xuly : xulyList) {
                    Object[] row = {xuly.getMaXL(), xuly.getMaTV(), xuly.getHinhThucXL(), xuly.getSoTien() , xuly.getNgayXL(), xuly.getTrangThaiXL()};
                    model.addRow(row);
                }
            } catch (NumberFormatException e) {
                        JOptionPane.showMessageDialog(null, "Vui lòng nhập số nguyên.", "Lỗi", JOptionPane.ERROR_MESSAGE);
                    }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

